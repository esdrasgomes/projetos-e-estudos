package exemploDouble;

public class exemploDouble {
	public static void main(String[] args) {
		
		double nota1 = 8;
		double nota2 = 7;
		double nota3 = 10;
		double nota4 = 9;
		
		double mediaFinal = (nota1 + nota2 + nota3 + nota4) / 4;
		
		System.out.println("A média do aluno é: " + mediaFinal);
		
	}
}
